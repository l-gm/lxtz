public class SimpleSms {

	/**
	 * 返回值
	 * */
	private String code;
	
	/**
	 * 返回结果描述
	 * */
	private String msg;
	
	/**
	 * 仅当提交成功后，此字段值才有意义（消息ID）
	 * */
	private String smsid;
	
	/**
	 * 剩余数量
	 * */
	private String num;

	
	
	/**
	 * 获取  返回值
	 * 
	 * @return
	 * */
	public String getCode() {
		return code;
	}

	/**
	 * 设置  返回值
	 * 
	 * @param code
	 * */
	public void setCode(String code) {
		this.code = code;
	}

	/**
	 * 获取  返回结果描述
	 * 
	 * @return 
	 * */
	public String getMsg() {
		return msg;
	}

	/**
	 * 设置  返回结果描述
	 * 
	 * @param msg
	 * */
	public void setMsg(String msg) {
		this.msg = msg;
	}

	/**
	 * 获取  仅当提交成功后，此字段值才有意义（消息ID）
	 * 
	 * @return
	 * */
	public String getSmsid() {
		return smsid;
	}

	/**
	 * 设置  仅当提交成功后，此字段值才有意义（消息ID）
	 * 
	 * @param smsid
	 * */
	public void setSmsid(String smsid) {
		this.smsid = smsid;
	}

	/**
	 * 获取  剩余数量
	 * 
	 * @return
	 * */
	public String getNum() {
		return num;
	}

	/**
	 * 设置  剩余数量
	 * 
	 * @param num
	 * */
	public void setNum(String num) {
		this.num = num;
	}
	
	
	
}
